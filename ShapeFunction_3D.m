function [N] = ShapeFunction_3D(s,t,w)

N1 = 1/8 * (1 - s) * (1 - t) * (1 - w);
N2 = 1/8 * (1 + s) * (1 - t) * (1 - w);
N3 = 1/8 * (1 + s) * (1 + t) * (1 - w);
N4 = 1/8 * (1 - s) * (1 + t) * (1 - w);
N5 = 1/8 * (1 - s) * (1 - t) * (1 + w);
N6 = 1/8 * (1 + s) * (1 - t) * (1 + w);
N7 = 1/8 * (1 + s) * (1 + t) * (1 + w);
N8 = 1/8 * (1 - s) * (1 + t) * (1 + w);

N=[N1,N2,N3,N4,N5,N6,N7,N8];

end